create table CONTACTOEMG
(
    ID_CONTACTOEMG number(6),
    NOMBRE varchar2(20) CONSTRAINT CONTACTOEMG_NOMBRE_NN NOT NULL,    
    APELLIDO varchar2(20),
    TELEFONO varchar2(15) CONSTRAINT CONTACTOEMG_TEL_NN NOT NULL,        
    ID_PERSONA NUMBER(6), CONSTRAINT FK_PERSONACEMG FOREIGN KEY (ID_PERSONA) REFERENCES PERSONA(ID_PERSONA),
    --Atributos Auditoria
    Fecha_creacion DATE,
    Usuario_creacion VARCHAR2(10),
    Fecha_ultima_modificacion DATE,
    Usuario_ultima_modificacion VARCHAR2(10)
);

ALTER table CONTACTOEMG add constraint pk_CONTACTOEMG primary key (ID_CONTACTOEMG)
using index tablespace ce_ind pctfree 20 
storage (initial 10k next 10k pctincrease 0);

