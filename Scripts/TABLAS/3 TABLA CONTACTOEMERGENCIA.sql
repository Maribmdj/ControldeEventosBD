--Comentarios de la tabla y atributos
COMMENT ON TABLE CONTACTOEMG IS 'TABLA DE LOS CONTACTOS DE EMERGENCIA DE PERSONA';
COMMENT ON COLUMN CE.CONTACTOEMG.ID_CONTACTOEMG IS 'IDENTIFICADOR ÚNICO DE LA TABLA';
COMMENT ON COLUMN CE.CONTACTOEMG.NOMBRE IS 'ATRIBUTO DEL NOMBRE DEL CONTACTO DE EMERGENCIA';
COMMENT ON COLUMN CE.CONTACTOEMG.APELLIDO IS 'ATRIBUTO CON EL APELLIDO DEL CONTACTO DE EMERGENCIA';
COMMENT ON COLUMN CE.CONTACTOEMG.TELEFONO IS 'ATRIBUTO CON EL TELEFONO DEL CONTACTO DE EMERGENCIA';
COMMENT ON COLUMN CE.CONTACTOEMG.ID_PERSONA IS 'ATRIBUTO REFERENCIADO DE LA TABLA PERSONA';

COMMENT ON COLUMN CE.CONTACTOEMG.Fecha_creacion IS 'ATRIBUTO AUDITORIA';
COMMENT ON COLUMN CE.CONTACTOEMG.Usuario_creacion IS 'ATRIBUTO AUDITOIA';
COMMENT ON COLUMN CE.CONTACTOEMG.Fecha_ultima_modificacion IS 'ATRIBUTO AUDITORIA';
COMMENT ON COLUMN CE.CONTACTOEMG.Usuario_ultima_modificacion IS 'ATRIBUTO AUDITORIA';
